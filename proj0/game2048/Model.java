    public static boolean emptySpaceExists(Board b) {
        //iterate through the board and check if the value of tile equals to null
        for(int col = 0; col < b.size(); col++) {
            for(int row = 0; row < b.size(); row++) {
                if(b.tile(col, row) == null) {
                    //if no tile, it is an empty space
                    return true;
                }
            }
        }
        return false;
    }

