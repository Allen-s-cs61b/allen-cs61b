    /** helper method for tilt, tilt operation in north direction */
    private void tilt(int size) {
        for(int col = 0; col < size; col++) {
            //store the top row that current tile can move to
            int tempRow = size - 1;
            //store the top tile
            Tile top = this.tile(col, size-1);
            for(int row = size - 2; row >= 0; row--) {
                Tile current = this.tile(col, row);
                //check if the current location has a tile
                if(current != null) {
                    //current location has a tile with value
                    if(top != null) {
                        //top location has a tile with value, check if two values are equal
                        if(current.value() != top.value()) {
                            //if two values are not equal, it can only move
                            tempRow--;
                            board.move(col, tempRow, current);
                        } else {
                            //two values are equal, it can only merge
                            board.move(col, tempRow, current);
                            score += this.board.tile(col, tempRow).value();
                            tempRow = tempRow - 1;
                        }
                        top = this.board.tile(col, tempRow);
                    } else {
                        //top location is empty
                        board.move(col, tempRow, current);
                        top = this.board.tile(col, tempRow);
                    }
                } else {
                    //current location does not have a tile
                    //nothing to change
                }
            }
        }
    }
    /** Return a copy of the original tiles */
    private Tile[][] tilesCopy(Board board) {
        Tile[][] copy = new Tile[board.size()][board.size()];
        for(int col = 0; col < board.size(); col++) {
            for(int row = 0; row < board.size(); row++) {
                copy[col][row] = board.tile(col, row);
            }
        }
        return copy;
    }
    /** Return true if any tile changes, false if not */
    private boolean compare(Tile[][] copy, Board board) {
        for(int col = 0; col < board.size(); col++) {
            for(int row = 0; row < board.size(); row++) {
                if(copy[col][row] != board.tile(col, row)) {
                    return true;
                }
            }
        }
        return false;
    }

